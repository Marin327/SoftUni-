function fruitOrVegetable([arg1]) {
        let product = arg1;
       if (product == "banana" || product == "apple" || product == "kiwi" || product == "cheery" || product == "lemon" || product == "grapes") {
               console.log("fruit");
       } else if (product == "tomato" || product == "cucumber" || product == "pepper" || product == "carrot") {
              console.log("vegetable"); 
      } else {
              console.log("unknown");
      }
}
fruitOrVegetable(["3434"]) 
